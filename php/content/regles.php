<h3>Le but du jeu</h3>
<p>Il s'agit de retrouver un mot choisi aléatoirement par
	l'ordinateur. Le joueur peut faire cinq propositions.</p>

<h3>Le déroulement du jeu</h3>
<p>On choisit un mot au hasard dans le dictionnaire. Le mot s'affiche en français (prmière ligne) et en anglais (deuxième ligne). C'est ce mot
	qui sera le mot à trouver par le joueur. Le joueur fait une
	proposition. Tant que les conditions de fin de partie ne sont pas
	atteintes, le joueur peut faire une nouvelle proposition.</p>

<h3>Une proposition</h3>
<p>Avant chaque proposition, on indique le nombre de lettres du
	mot à trouver, les lettres déjà découvertes sont affichées à leur
	place, et les lettres qu'on doit encore découvrir sont symbolisées
	par des tirets du bas.</p>

<p>Si la lettre proposée n'appartient pas au mon cherché, un morceau supplémentaire de l'image du pendu est dessiné. Lorsque une lettre proposée est correcte, on
	doit découvrir la (ou toutes les) occurence(s) de cette lettre dans
	le mot à trouver.</p>

<h3>Fin de partie</h3>
<p>La partie se termine lorsque</p>
<ul>
	<li>le pendu est complètement dessiné. Le joueur a alors perdu. Les lettres manquantes sont affichées en rouge</li>
	<li>le joueur a découvert le mot à trouver. Dans ce cas, le joueur a gagné.</li>
	<li>le joueur a décidé d'abandonner, et il a alors perdu.</li>
</ul>